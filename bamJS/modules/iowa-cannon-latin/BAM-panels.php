        The <i><b>Iowa Canon of Latin Authors and Works</i></b> is a catalogue and information repository for all extant Latin authors 
        and their writings, including fragmentary texts, as well as translations into Latin, 
        from the earliest period through the seventh century CE. 
        It includes geographic and chronological information, when available, and cross references to the PHI author list, the PL, the CPL, and DigiLibLT, an Italian database of non-Christian Latin authors from Late Antiquity. 
        The Iowa Canon was assembled in the spring of 2015 by members of Paul Dilley's graduate seminar on distant reading Latin Literature, 
        and will eventually serve as an important component of the <a href="http://www.lib.uiowa.edu/bam/" target="_blank">Big Ancient Mediterranean</a> project. 
        The interface is built and maintained by Ryan Horne, lead developer at BAM.
 
